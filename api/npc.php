<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '获取NPC信息':
        $NPC名 = isset($_POST['NPC名']) ? $_POST['NPC名'] : '';
        $NPC信息 = 获取NPC信息($NPC名);
        
        if (!$NPC信息) {
            JSON返回(false, 'NPC不存在');
        }
        
        $任务列表 = [];
        if (!empty($NPC信息['任务'])) {
            $任务名列表 = array_filter(array_map('trim', explode(',', $NPC信息['任务'])));
            $任务配置 = 读取配置('任务配置');
            foreach ($任务名列表 as $任务名) {
                if (isset($任务配置[$任务名])) {
                    $任务列表[] = [
                        '名称' => $任务名,
                        '描述' => $任务配置[$任务名]['描述'],
                        '类型' => $任务配置[$任务名]['类型']
                    ];
                }
            }
        }
        
        JSON返回(true, '获取成功', [
            'NPC名' => $NPC名,
            '描述' => $NPC信息['描述'],
            '位置' => $NPC信息['位置'],
            '商店' => $NPC信息['商店'],
            '任务列表' => $任务列表
        ]);
        break;
        
    case '与NPC对话':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $NPC名 = isset($_POST['NPC名']) ? $_POST['NPC名'] : '';
        $NPC信息 = 获取NPC信息($NPC名);
        
        if (!$NPC信息) {
            JSON返回(false, 'NPC不存在');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $对话内容 = "{$NPC名}：你好，欢迎来到{$NPC信息['位置']}！";
        
        if (!empty($NPC信息['商店'])) {
            $对话内容 .= "\\n这里有一家商店，需要看看吗？";
        }
        
        if (!empty($NPC信息['任务'])) {
            $对话内容 .= "\\n我这里有些任务，有兴趣吗？";
        }
        
        JSON返回(true, $对话内容);
        break;
        
    case '打开NPC商店':
        $NPC名 = isset($_POST['NPC名']) ? $_POST['NPC名'] : '';
        $NPC信息 = 获取NPC信息($NPC名);
        
        if (!$NPC信息 || empty($NPC信息['商店'])) {
            JSON返回(false, '该NPC没有商店');
        }
        
        $商店名 = $NPC信息['商店'];
        $商店配置 = 读取配置('商店配置');
        
        if (!isset($商店配置[$商店名])) {
            JSON返回(false, '商店不存在');
        }
        
        $物品列表 = [];
        if (!empty($商店配置[$商店名]['物品列表'])) {
            $物品名列表 = array_filter(array_map('trim', explode(',', $商店配置[$商店名]['物品列表'])));
            foreach ($物品名列表 as $物品名) {
                $物品信息 = 获取物品信息($物品名);
                if ($物品信息) {
                    $物品列表[] = [
                        '名称' => $物品名,
                        '类型' => $物品信息['类型'],
                        '等级' => $物品信息['等级'],
                        '描述' => $物品信息['描述'],
                        '价格' => $物品信息['价格']
                    ];
                }
            }
        }
        
        JSON返回(true, '获取成功', [
            '商店名' => $商店名,
            'NPC名' => $NPC名,
            '物品列表' => $物品列表
        ]);
        break;
        
    case '查看玩家':
        $用户名 = isset($_POST['用户名']) ? $_POST['用户名'] : '';
        $用户数据 = 读取用户数据($用户名);
        
        if (!$用户数据 || !isset($用户数据['角色'])) {
            JSON返回(false, '玩家不存在');
        }
        
        JSON返回(true, '获取成功', [
            '用户名' => $用户名,
            '角色名' => $用户数据['角色']['角色名'],
            '等级' => $用户数据['角色']['等级'],
            '门派' => $用户数据['角色']['门派'],
            '位置' => $用户数据['角色']['位置']
        ]);
        break;
        
    case '赠送物品':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $目标玩家 = isset($_POST['目标玩家']) ? $_POST['目标玩家'] : '';
        $物品名 = isset($_POST['物品名']) ? $_POST['物品名'] : '';
        $数量 = isset($_POST['数量']) ? intval($_POST['数量']) : 1;
        
        $发送者 = $_SESSION['用户名'];
        $发送者数据 = 读取用户数据($发送者);
        $接收者数据 = 读取用户数据($目标玩家);
        
        if (!$接收者数据) {
            JSON返回(false, '目标玩家不存在');
        }
        
        if (!isset($发送者数据['背包'][$物品名]) || $发送者数据['背包'][$物品名] < $数量) {
            JSON返回(false, '物品数量不足');
        }
        
        从背包移除物品($发送者数据, $物品名, $数量);
        添加物品到背包($接收者数据, $物品名, $数量);
        
        写入用户数据($发送者, $发送者数据);
        写入用户数据($目标玩家, $接收者数据);
        
        JSON返回(true, "成功赠送{$数量}个{$物品名}给{$目标玩家}");
        break;
        
    case '赠送货币':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $目标玩家 = isset($_POST['目标玩家']) ? $_POST['目标玩家'] : '';
        $数量 = isset($_POST['数量']) ? intval($_POST['数量']) : 0;
        
        if ($数量 <= 0) {
            JSON返回(false, '赠送数量必须大于0');
        }
        
        $发送者 = $_SESSION['用户名'];
        $发送者数据 = 读取用户数据($发送者);
        $接收者数据 = 读取用户数据($目标玩家);
        
        if (!$接收者数据) {
            JSON返回(false, '目标玩家不存在');
        }
        
        if ($发送者数据['角色']['货币'] < $数量) {
            JSON返回(false, '货币不足');
        }
        
        $发送者数据['角色']['货币'] -= $数量;
        $接收者数据['角色']['货币'] += $数量;
        
        写入用户数据($发送者, $发送者数据);
        写入用户数据($目标玩家, $接收者数据);
        
        JSON返回(true, "成功赠送{$数量}货币给{$目标玩家}");
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

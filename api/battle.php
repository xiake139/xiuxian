<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

function 计算伤害($攻击, $防御) {
    $基础伤害 = max(1, $攻击 - $防御 * 0.5);
    $浮动 = rand(90, 110) / 100;
    return intval($基础伤害 * $浮动);
}

function 解析技能伤害($技能信息, $攻击者攻击) {
    $伤害公式 = $技能信息['伤害'];
    if (preg_match('/基础攻击\*([\d.]+)/', $伤害公式, $匹配)) {
        $倍率 = floatval($匹配[1]);
        return intval($攻击者攻击 * $倍率);
    }
    return $攻击者攻击;
}


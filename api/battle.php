            $效果 = $技能信息['效果'];
            if (preg_match('/临时提升防御(\d+)%/', $效果, $匹配)) {
                $提升百分比 = intval($匹配[1]);
                $原防御 = $战斗['玩家防御'];
                $战斗['玩家防御'] = intval($原防御 * (1 + $提升百分比 / 100));
                $日志[] = "你使用{$技能名}，防御力提升了！";

<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '创建角色':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $角色名 = isset($_POST['角色名']) ? trim($_POST['角色名']) : '';
        $性别 = isset($_POST['性别']) ? $_POST['性别'] : '男';
        $门派 = isset($_POST['门派']) ? $_POST['门派'] : '青云派';
        
        if (empty($角色名)) {
            JSON返回(false, '角色名不能为空');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        if ($用户数据['账号']['是否已创建角色'] == '是') {
            JSON返回(false, '角色已存在');
        }
        
        $系统配置 = 读取配置('系统配置');
        
        $用户数据['角色'] = [
            '角色名' => $角色名,
            '性别' => $性别,
            '门派' => $门派,
            '等级' => $系统配置['游戏设置']['初始等级'],
            '经验' => $系统配置['游戏设置']['初始经验'],
            '货币' => $系统配置['游戏设置']['初始货币'],
            '生命' => 100,
            '最大生命' => 100,
            '灵力' => 50,
            '最大灵力' => 50,
            '攻击' => 10,
            '防御' => 5,
            '位置' => '青云村'
        ];
        
        $用户数据['装备'] = [
            '武器' => '',
            '防具' => '',
            '饰品' => ''
        ];
        
        $用户数据['背包'] = [
            '金创药' => 5
        ];
        
        $用户数据['技能'] = [
            '已学技能' => '基础剑术,铁布衫'
        ];
        
        $用户数据['任务'] = [
            '已接任务' => '',
            '已完成任务' => ''
        ];
        
        $用户数据['签到'] = [
            '最后签到日期' => '',
            '连续签到天数' => 0
        ];
        
        $用户数据['战斗统计'] = [
            '击杀怪物数' => 0,
            '死亡次数' => 0
        ];
        
        $用户数据['账号']['是否已创建角色'] = '是';
        
        if (写入用户数据($用户名, $用户数据)) {
            JSON返回(true, '角色创建成功');
        } else {
            JSON返回(false, '角色创建失败');
        }
        break;
        
    case '获取角色信息':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户数据 = 获取当前用户();
        if (!$用户数据 || !isset($用户数据['角色'])) {
            JSON返回(false, '角色不存在');
        }
        
        JSON返回(true, '获取成功', $用户数据['角色']);
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

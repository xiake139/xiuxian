<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

function 计算总属性($用户数据) {
    $基础属性 = $用户数据['角色'];
    $装备 = isset($用户数据['装备']) ? $用户数据['装备'] : [];
    $总攻击 = $基础属性['攻击'];
    $总防御 = $基础属性['防御'];
    $总生命 = $基础属性['最大生命'];
    $总灵力 = $基础属性['最大灵力'];
    
    foreach ($装备 as $部位 => $物品名) {
        if (!empty($物品名)) {
            $物品信息 = 获取物品信息($物品名);
            if ($物品信息) {
                $总攻击 += $物品信息['攻击'];
                $总防御 += $物品信息['防御'];
                $总生命 += $物品信息['生命'];
            }
        }
    }
    
    return [
        '攻击' => $总攻击,
        '防御' => $总防御,
        '最大生命' => $总生命,
        '最大灵力' => $总灵力
    ];
}

switch ($动作) {
    case '获取装备':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户数据 = 获取当前用户();
        $装备 = isset($用户数据['装备']) ? $用户数据['装备'] : [
            '武器' => '',
            '防具' => '',
            '饰品' => ''
        ];
        
        $装备详情 = [];
        foreach ($装备 as $部位 => $物品名) {
            if (!empty($物品名)) {
                $物品信息 = 获取物品信息($物品名);
                $装备详情[$部位] = [
                    '名称' => $物品名,
                    '信息' => $物品信息
                ];
            } else {
                $装备详情[$部位] = null;
            }
        }
        
        $总属性 = 计算总属性($用户数据);
        
        JSON返回(true, '获取成功', [
            '装备' => $装备详情,
            '总属性' => $总属性
        ]);
        break;
        
    case '装备物品':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $物品名 = isset($_POST['物品名']) ? $_POST['物品名'] : '';
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        if (!isset($用户数据['背包'][$物品名]) || $用户数据['背包'][$物品名] <= 0) {
            JSON返回(false, '物品不足');
        }
        
        $物品信息 = 获取物品信息($物品名);
        if (!$物品信息) {
            JSON返回(false, '物品不存在');
        }
        
        $类型 = $物品信息['类型'];
        $部位 = '';
        if ($类型 == '武器') {
            $部位 = '武器';
        } elseif ($类型 == '防具') {
            $部位 = '防具';
        } elseif ($类型 == '饰品') {
            $部位 = '饰品';
        } else {
            JSON返回(false, '该物品无法装备');
        }
        
        if (!isset($用户数据['装备'])) {
            $用户数据['装备'] = [
                '武器' => '',
                '防具' => '',
                '饰品' => ''
            ];
        }
        
        if (!empty($用户数据['装备'][$部位])) {
            添加物品到背包($用户数据, $用户数据['装备'][$部位], 1);
        }
        
        $用户数据['装备'][$部位] = $物品名;
        从背包移除物品($用户数据, $物品名, 1);
        
        写入用户数据($用户名, $用户数据);
        $总属性 = 计算总属性($用户数据);
        
        JSON返回(true, '装备成功', [
            '装备' => $用户数据['装备'],
            '总属性' => $总属性
        ]);
        break;
        
    case '卸下装备':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $部位 = isset($_POST['部位']) ? $_POST['部位'] : '';
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        if (!isset($用户数据['装备'][$部位]) || empty($用户数据['装备'][$部位])) {
            JSON返回(false, '该部位没有装备');
        }
        
        $物品名 = $用户数据['装备'][$部位];
        添加物品到背包($用户数据, $物品名, 1);
        $用户数据['装备'][$部位] = '';
        
        写入用户数据($用户名, $用户数据);
        $总属性 = 计算总属性($用户数据);
        
        JSON返回(true, '卸下成功', [
            '装备' => $用户数据['装备'],
            '总属性' => $总属性
        ]);
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

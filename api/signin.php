<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '签到':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        $今天 = date('Y-m-d');
        
        if (!isset($用户数据['签到'])) {
            $用户数据['签到'] = [
                '最后签到日期' => '',
                '连续签到天数' => 0
            ];
        }
        
        if ($用户数据['签到']['最后签到日期'] == $今天) {
            JSON返回(false, '今天已经签到过了');
        }
        
        $昨天 = date('Y-m-d', strtotime('-1 day'));
        if ($用户数据['签到']['最后签到日期'] == $昨天) {
            $用户数据['签到']['连续签到天数'] += 1;
        } else {
            $用户数据['签到']['连续签到天数'] = 1;
        }
        
        $系统配置 = 读取配置('系统配置');
        $奖励货币 = $系统配置['游戏设置']['每次签到奖励货币'];
        $奖励经验 = $系统配置['游戏设置']['每次签到奖励经验'];
        
        $连续天数 = $用户数据['签到']['连续签到天数'];
        $奖励货币 = intval($奖励货币 * (1 + ($连续天数 - 1) * 0.1));
        $奖励经验 = intval($奖励经验 * (1 + ($连续天数 - 1) * 0.1));
        
        $用户数据['签到']['最后签到日期'] = $今天;
        $用户数据['角色']['货币'] += $奖励货币;
        $用户数据['角色']['经验'] += $奖励经验;
        
        检查升级($用户数据);
        写入用户数据($用户名, $用户数据);
        
        JSON返回(true, "签到成功！连续签到{$连续天数}天\\n获得货币：{$奖励货币}\\n获得经验：{$奖励经验}", [
            '连续天数' => $连续天数,
            '奖励货币' => $奖励货币,
            '奖励经验' => $奖励经验
        ]);
        break;
        
    case '获取签到信息':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        $今天 = date('Y-m-d');
        
        $已签到 = false;
        $连续天数 = 0;
        
        if (isset($用户数据['签到'])) {
            $已签到 = $用户数据['签到']['最后签到日期'] == $今天;
            $连续天数 = intval($用户数据['签到']['连续签到天数']);
        }
        
        JSON返回(true, '获取成功', [
            '今天已签到' => $已签到,
            '连续签到天数' => $连续天数
        ]);
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

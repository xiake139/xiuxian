<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '获取当前地图':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户数据 = 获取当前用户();
        $地图名 = $用户数据['角色']['位置'];
        $地图信息 = 获取地图信息($地图名);
        
        if (!$地图信息) {
            JSON返回(false, '地图不存在');
        }
        
        $NPC列表 = [];
        if (!empty($地图信息['NPC'])) {
            $NPC列表 = array_filter(array_map('trim', explode(',', $地图信息['NPC'])));
        }
        
        $怪物列表 = [];
        if (!empty($地图信息['怪物'])) {
            $怪物列表 = array_filter(array_map('trim', explode(',', $地图信息['怪物'])));
        }
        
        $玩家列表 = 获取当前地图玩家($地图名);
        $其他玩家 = [];
        foreach ($玩家列表 as $玩家) {
            if ($玩家['用户名'] != $_SESSION['用户名']) {
                $其他玩家[] = $玩家;
            }
        }
        
        $可移动方向 = [];
        foreach (['前', '后', '左', '右'] as $方向) {
            if (!empty($地图信息[$方向])) {
                $可移动方向[$方向] = $地图信息[$方向];
            }
        }
        
        JSON返回(true, '获取成功', [
            '地图名' => $地图名,
            '描述' => $地图信息['描述'],
            'NPC列表' => $NPC列表,
            '怪物列表' => $怪物列表,
            '玩家列表' => $其他玩家,
            '可移动方向' => $可移动方向
        ]);
        break;
        
    case '移动':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $方向 = isset($_POST['方向']) ? $_POST['方向'] : '';
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        $当前地图 = $用户数据['角色']['位置'];
        $地图信息 = 获取地图信息($当前地图);
        
        if (empty($地图信息[$方向])) {
            JSON返回(false, '该方向无法移动');
        }
        
        $目标地图 = $地图信息[$方向];
        $用户数据['角色']['位置'] = $目标地图;
        写入用户数据($用户名, $用户数据);
        
        JSON返回(true, '移动成功', [
            '目标地图' => $目标地图
        ]);
        break;
        
    case '获取所有地图':
        $地图配置 = 读取配置('地图配置');
        $地图列表 = [];
        foreach ($地图配置 as $地图名 => $信息) {
            $地图列表[] = [
                '名称' => $地图名,
                '描述' => $信息['描述']
            ];
        }
        JSON返回(true, '获取成功', $地图列表);
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

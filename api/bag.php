<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '获取背包':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户数据 = 获取当前用户();
        $背包 = isset($用户数据['背包']) ? $用户数据['背包'] : [];
        
        $物品列表 = [];
        foreach ($背包 as $物品名 => $数量) {
            $物品信息 = 获取物品信息($物品名);
            if ($物品信息) {
                $物品列表[] = [
                    '名称' => $物品名,
                    '数量' => $数量,
                    '类型' => $物品信息['类型'],
                    '等级' => $物品信息['等级'],
                    '描述' => $物品信息['描述'],
                    '价格' => $物品信息['价格']
                ];
            }
        }
        
        JSON返回(true, '获取成功', $物品列表);
        break;
        
    case '使用物品':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $物品名 = isset($_POST['物品名']) ? $_POST['物品名'] : '';
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        if (!isset($用户数据['背包'][$物品名]) || $用户数据['背包'][$物品名] <= 0) {
            JSON返回(false, '物品不足');
        }
        
        $物品信息 = 获取物品信息($物品名);
        if (!$物品信息) {
            JSON返回(false, '物品不存在');
        }
        
        $使用成功 = false;
        $消息 = '';
        
        if ($物品信息['类型'] == '药品' || $物品信息['类型'] == '丹药') {
            $效果 = $物品信息['效果'];
            if (strpos($效果, '恢复生命') !== false) {
                preg_match('/恢复生命(\d+)/', $效果, $匹配);
                if ($匹配) {
                    $恢复量 = $匹配[1];
                    $用户数据['角色']['生命'] = min($用户数据['角色']['生命'] + $恢复量, $用户数据['角色']['最大生命']);
                    $消息 = "使用{$物品名}，恢复生命{$恢复量}点";
                    $使用成功 = true;
                }
            } elseif (strpos($效果, '恢复灵力') !== false) {
                preg_match('/恢复灵力(\d+)/', $效果, $匹配);
                if ($匹配) {
                    $恢复量 = $匹配[1];
                    $用户数据['角色']['灵力'] = min($用户数据['角色']['灵力'] + $恢复量, $用户数据['角色']['最大灵力']);
                    $消息 = "使用{$物品名}，恢复灵力{$恢复量}点";
                    $使用成功 = true;
                }
            }
        }
        
        if ($使用成功) {
            从背包移除物品($用户数据, $物品名, 1);
            写入用户数据($用户名, $用户数据);
            JSON返回(true, $消息, $用户数据['角色']);
        } else {
            JSON返回(false, '该物品无法使用');
        }
        break;
        
    case '丢弃物品':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $物品名 = isset($_POST['物品名']) ? $_POST['物品名'] : '';
        $数量 = isset($_POST['数量']) ? intval($_POST['数量']) : 1;
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        if (从背包移除物品($用户数据, $物品名, $数量)) {
            写入用户数据($用户名, $用户数据);
            JSON返回(true, '丢弃成功');
        } else {
            JSON返回(false, '物品数量不足');
        }
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

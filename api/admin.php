        }
        
        $商店配置 = 读取配置('商店配置');
        if (isset($商店配置[$商店名])) {
            JSON返回(false, '商店已存在');
        }
        
        $商店配置[$商店名] = [
            '物品' => $物品,
        ];
        
        写入配置('商店配置', $商店配置);
        JSON返回(true, '商店添加成功');
        break;
        
    case '添加配置节':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $配置文件名 = isset($_POST['配置文件名']) ? $_POST['配置文件名'] : '';
        $节名 = isset($_POST['节名']) ? $_POST['节名'] : '';
        $数据JSON = isset($_POST['数据']) ? $_POST['数据'] : '{}';
        
        if (!$配置文件名 || !$节名) {
            JSON返回(false, '配置文件名和节名不能为空');
        }
        
        $配置数据 = 读取配置($配置文件名);
        if (isset($配置数据[$节名])) {
            JSON返回(false, '该节已存在，请使用编辑功能');
        }
        
        $数据 = json_decode($数据JSON, true);
        if (json_last_error() !== JSON_ERROR_NONE) {
            JSON返回(false, '数据格式错误');
        }
        
        $配置数据[$节名] = $数据;
        写入配置($配置文件名, $配置数据);
        JSON返回(true, '添加成功');
        break;
        
    case '编辑配置节':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $配置文件名 = isset($_POST['配置文件名']) ? $_POST['配置文件名'] : '';
        $节名 = isset($_POST['节名']) ? $_POST['节名'] : '';
        $数据JSON = isset($_POST['数据']) ? $_POST['数据'] : '{}';
        
        if (!$配置文件名 || !$节名) {
            JSON返回(false, '配置文件名和节名不能为空');
        }
        
        $配置数据 = 读取配置($配置文件名);
        if (!isset($配置数据[$节名])) {
            JSON返回(false, '该节不存在');
        }
        
        $数据 = json_decode($数据JSON, true);
        if (json_last_error() !== JSON_ERROR_NONE) {
            JSON返回(false, '数据格式错误');
        }
        
        $配置数据[$节名] = $数据;
        写入配置($配置文件名, $配置数据);
        JSON返回(true, '编辑成功');
        break;
        
    case '删除配置节':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $配置文件名 = isset($_POST['配置文件名']) ? $_POST['配置文件名'] : '';
        $节名 = isset($_POST['节名']) ? $_POST['节名'] : '';
        
        if (!$配置文件名 || !$节名) {
            JSON返回(false, '配置文件名和节名不能为空');
        }
        
        $配置数据 = 读取配置($配置文件名);
        if (!isset($配置数据[$节名])) {
            JSON返回(false, '该节不存在');
        }
        
        unset($配置数据[$节名]);
        写入配置($配置文件名, $配置数据);
        JSON返回(true, '删除成功');
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}

function 生成唯一坐标($已使用坐标) {
    $尝试次数 = 0;
    while ($尝试次数 < 100) {
        $x = rand(-10, 10);
        $y = rand(-10, 10);
        $坐标 = "$x,$y";
        if (!in_array($坐标, $已使用坐标)) {
            return $坐标;
        }
        $尝试次数++;
    }
    return "0,0";
}

function 自动连接地图(&$地图配置, $新地图名) {
    $新地图 = &$地图配置[$新地图名];
    
    foreach ($地图配置 as $地图名 => &$地图) {
        if ($地图名 === $新地图名) continue;
        
        if (!empty($新地图['前']) && $新地图['前'] === $地图名) {
            $地图['后'] = $新地图名;
        }
        
        if (!empty($新地图['后']) && $新地图['后'] === $地图名) {
            $地图['前'] = $新地图名;
        }
        
        if (!empty($新地图['左']) && $新地图['左'] === $地图名) {
            $地图['右'] = $新地图名;
        }
        
        if (!empty($新地图['右']) && $新地图['右'] === $地图名) {
            $地图['左'] = $新地图名;
        }
    }
    
    if (!empty($新地图['前']) || !empty($新地图['后']) || !empty($新地图['左']) || !empty($新地图['右'])) {
        return;
    }
    
    foreach ($地图配置 as $地图名 => &$地图) {
        if ($地图名 === $新地图名) continue;
        
        if (empty($新地图['前']) && empty($地图['后'])) {
            $新地图['前'] = $地图名;
            $地图['后'] = $新地图名;
            return;
        }
        
        if (empty($新地图['后']) && empty($地图['前'])) {
            $新地图['后'] = $地图名;
            $地图['前'] = $新地图名;
            return;
        }
        
        if (empty($新地图['左']) && empty($地图['右'])) {
            $新地图['左'] = $地图名;
            $地图['右'] = $新地图名;
            return;
        }
        
        if (empty($新地图['右']) && empty($地图['左'])) {
            $新地图['右'] = $地图名;
            $地图['左'] = $新地图名;
            return;
        }
    }
}

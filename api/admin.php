<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

function 验证管理员() {
    $系统配置 = 读取配置('系统配置');
    $管理员账号 = $系统配置['游戏设置']['管理员账号'];
    $管理员密码 = $系统配置['游戏设置']['管理员密码'];
    
    $输入账号 = isset($_POST['管理员账号']) ? $_POST['管理员账号'] : '';
    $输入密码 = isset($_POST['管理员密码']) ? $_POST['管理员密码'] : '';
    
    if (!isset($_SESSION['管理员登录']) || $_SESSION['管理员登录'] !== true) {
        if ($输入账号 === $管理员账号 && $输入密码 === $管理员密码) {
            $_SESSION['管理员登录'] = true;
            return true;
        }
        return false;
    }
    return true;
}

switch ($动作) {
    case '管理员登录':
        if (验证管理员()) {
            JSON返回(true, '管理员登录成功');
        } else {
            JSON返回(false, '管理员账号或密码错误');
        }
        break;
        
    case '获取所有用户':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $用户列表 = [];
        $目录 = DATA_PATH . '/users';
        if (is_dir($目录)) {
            $文件 = scandir($目录);
            foreach ($文件 as $文件名) {
                if (pathinfo($文件名, PATHINFO_EXTENSION) == 'ini') {
                    $用户名 = pathinfo($文件名, PATHINFO_FILENAME);
                    $用户数据 = 读取用户数据($用户名);
                    $用户列表[] = [
                        '用户名' => $用户名,
                        '角色名' => isset($用户数据['角色']['角色名']) ? $用户数据['角色']['角色名'] : '未创建',
                        '等级' => isset($用户数据['角色']['等级']) ? $用户数据['角色']['等级'] : 0,
                        '是否已创建角色' => $用户数据['账号']['是否已创建角色']
                    ];
                }
            }
        }
        
        JSON返回(true, '获取成功', $用户列表);
        break;
        
    case '读取用户数据':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $用户名 = isset($_POST['用户名']) ? $_POST['用户名'] : '';
        $用户数据 = 读取用户数据($用户名);
        
        if (!$用户数据) {
            JSON返回(false, '用户不存在');
        }
        
        JSON返回(true, '获取成功', $用户数据);
        break;
        
    case '写入用户数据':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $用户名 = isset($_POST['用户名']) ? $_POST['用户名'] : '';
        $节 = isset($_POST['节']) ? $_POST['节'] : '';
        $键 = isset($_POST['键']) ? $_POST['键'] : '';
        $值 = isset($_POST['值']) ? $_POST['值'] : '';
        
        $用户数据 = 读取用户数据($用户名);
        if (!$用户数据) {
            JSON返回(false, '用户不存在');
        }
        
        if (!isset($用户数据[$节])) {
            $用户数据[$节] = [];
        }
        $用户数据[$节][$键] = $值;
        
        写入用户数据($用户名, $用户数据);
        JSON返回(true, '写入成功');
        break;
        
    case '删除用户':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $用户名 = isset($_POST['用户名']) ? $_POST['用户名'] : '';
        $路径 = DATA_PATH . '/users/' . $用户名 . '.ini';
        
        if (!file_exists($路径)) {
            JSON返回(false, '用户不存在');
        }
        
        if (unlink($路径)) {
            JSON返回(true, '用户删除成功');
        } else {
            JSON返回(false, '删除失败');
        }
        break;
        
    case '重置用户数据':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $用户名 = isset($_POST['用户名']) ? $_POST['用户名'] : '';
        $用户数据 = 读取用户数据($用户名);
        
        if (!$用户数据) {
            JSON返回(false, '用户不存在');
        }
        
        $系统配置 = 读取配置('系统配置');
        
        $用户数据['角色'] = [
            '角色名' => $用户数据['角色']['角色名'],
            '性别' => $用户数据['角色']['性别'],
            '门派' => $用户数据['角色']['门派'],
            '等级' => $系统配置['游戏设置']['初始等级'],
            '经验' => $系统配置['游戏设置']['初始经验'],
            '货币' => $系统配置['游戏设置']['初始货币'],
            '生命' => 100,
            '最大生命' => 100,
            '灵力' => 50,
            '最大灵力' => 50,
            '攻击' => 10,
            '防御' => 5,
            '位置' => '青云村'
        ];
        
        $用户数据['装备'] = [
            '武器' => '',
            '防具' => '',
            '饰品' => ''
        ];
        
        $用户数据['背包'] = [
            '金创药' => 5
        ];
        
        $用户数据['技能'] = [
            '已学技能' => '基础剑术,铁布衫'
        ];
        
        $用户数据['任务'] = [
            '已接任务' => '',
            '已完成任务' => ''
        ];
        
        $用户数据['任务进度'] = [];
        
        $用户数据['签到'] = [
            '最后签到日期' => '',
            '连续签到天数' => 0
        ];
        
        $用户数据['战斗统计'] = [
            '击杀怪物数' => 0,
            '死亡次数' => 0
        ];
        
        写入用户数据($用户名, $用户数据);
        JSON返回(true, '用户数据重置成功');
        break;
        
    case '读取配置文件':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $配置文件名 = isset($_POST['配置文件名']) ? $_POST['配置文件名'] : '';
        $配置数据 = 读取配置($配置文件名);
        
        if (empty($配置数据)) {
            JSON返回(false, '配置文件不存在或为空');
        }
        
        JSON返回(true, '获取成功', $配置数据);
        break;
        
    case '写入配置文件':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $配置文件名 = isset($_POST['配置文件名']) ? $_POST['配置文件名'] : '';
        $节 = isset($_POST['节']) ? $_POST['节'] : '';
        $键 = isset($_POST['键']) ? $_POST['键'] : '';
        $值 = isset($_POST['值']) ? $_POST['值'] : '';
        
        $配置数据 = 读取配置($配置文件名);
        if (!isset($配置数据[$节])) {
            $配置数据[$节] = [];
        }
        $配置数据[$节][$键] = $值;
        
        写入配置($配置文件名, $配置数据);
        JSON返回(true, '配置写入成功');
        break;
        
    case '重置所有数据':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $目录 = DATA_PATH . '/users';
        if (is_dir($目录)) {
            $文件 = scandir($目录);
            foreach ($文件 as $文件名) {
                if (pathinfo($文件名, PATHINFO_EXTENSION) == 'ini') {
                    $路径 = $目录 . '/' . $文件名;
                    unlink($路径);
                }
            }
        }
        
        JSON返回(true, '所有用户数据已重置');
        break;
        
    case '获取所有配置文件':
        if (!验证管理员()) {
            JSON返回(false, '请先登录管理员账号');
        }
        
        $配置文件列表 = [];
        $目录 = CONFIG_PATH;
        if (is_dir($目录)) {
            $文件 = scandir($目录);
            foreach ($文件 as $文件名) {
                if (pathinfo($文件名, PATHINFO_EXTENSION) == 'ini') {
                    $配置文件列表[] = pathinfo($文件名, PATHINFO_FILENAME);
                }
            }
        }
        
        JSON返回(true, '获取成功', $配置文件列表);
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

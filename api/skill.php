<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '获取技能列表':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户数据 = 获取当前用户();
        $已学技能 = isset($用户数据['技能']['已学技能']) ? $用户数据['技能']['已学技能'] : '';
        $已学列表 = array_filter(array_map('trim', explode(',', $已学技能)));
        
        $技能配置 = 读取配置('技能配置');
        $所有技能 = [];
        
        foreach ($技能配置 as $技能名 => $技能信息) {
            $所有技能[] = [
                '名称' => $技能名,
                '类型' => $技能信息['类型'],
                '等级' => $技能信息['等级'],
                '描述' => $技能信息['描述'],
                '消耗灵力' => $技能信息['消耗灵力'],
                '已学' => in_array($技能名, $已学列表)
            ];
        }
        
        JSON返回(true, '获取成功', [
            '已学技能' => $已学列表,
            '所有技能' => $所有技能
        ]);
        break;
        
    case '学习技能':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $技能名 = isset($_POST['技能名']) ? $_POST['技能名'] : '';
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $技能信息 = 获取技能信息($技能名);
        if (!$技能信息) {
            JSON返回(false, '技能不存在');
        }
        
        $已学技能 = isset($用户数据['技能']['已学技能']) ? $用户数据['技能']['已学技能'] : '';
        $已学列表 = array_filter(array_map('trim', explode(',', $已学技能)));
        
        if (in_array($技能名, $已学列表)) {
            JSON返回(false, '已学习该技能');
        }
        
        $学习条件 = $技能信息['学习条件'];
        if (preg_match('/等级(\d+)/', $学习条件, $匹配)) {
            $需要等级 = intval($匹配[1]);
            if ($用户数据['角色']['等级'] < $需要等级) {
                JSON返回(false, "等级不足，需要等级{$需要等级}");
            }
        }
        
        $已学列表[] = $技能名;
        $用户数据['技能']['已学技能'] = implode(',', $已学列表);
        写入用户数据($用户名, $用户数据);
        
        JSON返回(true, "学习{$技能名}成功");
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

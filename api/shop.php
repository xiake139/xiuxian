<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '获取商店物品':
        $商店名 = isset($_POST['商店名']) ? $_POST['商店名'] : '系统商店';
        $商店配置 = 读取配置('商店配置');
        
        if (!isset($商店配置[$商店名])) {
            JSON返回(false, '商店不存在');
        }
        
        $物品列表 = [];
        if (!empty($商店配置[$商店名]['物品列表'])) {
            $物品名列表 = array_filter(array_map('trim', explode(',', $商店配置[$商店名]['物品列表'])));
            foreach ($物品名列表 as $物品名) {
                $物品信息 = 获取物品信息($物品名);
                if ($物品信息) {
                    $物品列表[] = [
                        '名称' => $物品名,
                        '类型' => $物品信息['类型'],
                        '等级' => $物品信息['等级'],
                        '描述' => $物品信息['描述'],
                        '价格' => $物品信息['价格']
                    ];
                }
            }
        }
        
        JSON返回(true, '获取成功', [
            '商店名' => $商店名,
            '物品列表' => $物品列表
        ]);
        break;
        
    case '购买物品':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $物品名 = isset($_POST['物品名']) ? $_POST['物品名'] : '';
        $数量 = isset($_POST['数量']) ? intval($_POST['数量']) : 1;
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $物品信息 = 获取物品信息($物品名);
        if (!$物品信息) {
            JSON返回(false, '物品不存在');
        }
        
        $总价 = $物品信息['价格'] * $数量;
        if ($用户数据['角色']['货币'] < $总价) {
            JSON返回(false, '货币不足');
        }
        
        $用户数据['角色']['货币'] -= $总价;
        添加物品到背包($用户数据, $物品名, $数量);
        写入用户数据($用户名, $用户数据);
        
        JSON返回(true, "购买成功，花费{$总价}货币", [
            '剩余货币' => $用户数据['角色']['货币']
        ]);
        break;
        
    case '出售物品':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $物品名 = isset($_POST['物品名']) ? $_POST['物品名'] : '';
        $数量 = isset($_POST['数量']) ? intval($_POST['数量']) : 1;
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        if (!isset($用户数据['背包'][$物品名]) || $用户数据['背包'][$物品名] < $数量) {
            JSON返回(false, '物品数量不足');
        }
        
        $物品信息 = 获取物品信息($物品名);
        if (!$物品信息) {
            JSON返回(false, '物品不存在');
        }
        
        $出售价 = intval($物品信息['价格'] * 0.5) * $数量;
        从背包移除物品($用户数据, $物品名, $数量);
        $用户数据['角色']['货币'] += $出售价;
        写入用户数据($用户名, $用户数据);
        
        JSON返回(true, "出售成功，获得{$出售价}货币", [
            '剩余货币' => $用户数据['角色']['货币']
        ]);
        break;
        
    case '获取系统商店':
        header('Location: shop.php?动作=获取商店物品&商店名=系统商店');
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>

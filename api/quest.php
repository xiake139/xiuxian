<?php
require_once 'core.php';

$动作 = isset($_POST['动作']) ? $_POST['动作'] : '';

switch ($动作) {
    case '获取可接任务':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $NPC名 = isset($_POST['NPC名']) ? $_POST['NPC名'] : '';
        $NPC信息 = 获取NPC信息($NPC名);
        
        if (!$NPC信息) {
            JSON返回(false, 'NPC不存在');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $已接任务 = isset($用户数据['任务']['已接任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已接任务']))) : [];
        $已完成任务 = isset($用户数据['任务']['已完成任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已完成任务']))) : [];
        
        $任务配置 = 读取配置('任务配置');
        $NPC任务 = isset($NPC信息['任务']) ? array_filter(array_map('trim', explode(',', $NPC信息['任务']))) : [];
        
        $可接任务 = [];
        foreach ($NPC任务 as $任务名) {
            if (isset($任务配置[$任务名])) {
                $任务 = $任务配置[$任务名];
                $前置任务 = isset($任务['前置任务']) ? trim($任务['前置任务']) : '';
                
                $可接 = true;
                if (in_array($任务名, $已接任务) || in_array($任务名, $已完成任务)) {
                    $可接 = false;
                }
                if (!empty($前置任务) && !in_array($前置任务, $已完成任务)) {
                    $可接 = false;
                }
                
                if ($可接) {
                    $可接任务[] = [
                        '名称' => $任务名,
                        '描述' => $任务['描述'],
                        '类型' => $任务['类型'],
                        '奖励经验' => $任务['奖励经验'],
                        '奖励货币' => $任务['奖励货币'],
                        '奖励物品' => isset($任务['奖励物品']) ? $任务['奖励物品'] : '',
                        '奖励物品数量' => isset($任务['奖励物品数量']) ? $任务['奖励物品数量'] : 0
                    ];
                }
            }
        }
        
        JSON返回(true, '获取成功', $可接任务);
        break;
        
    case '接受任务':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $任务名 = isset($_POST['任务名']) ? $_POST['任务名'] : '';
        $任务配置 = 读取配置('任务配置');
        
        if (!isset($任务配置[$任务名])) {
            JSON返回(false, '任务不存在');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $已接任务 = isset($用户数据['任务']['已接任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已接任务']))) : [];
        $已完成任务 = isset($用户数据['任务']['已完成任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已完成任务']))) : [];
        
        if (in_array($任务名, $已接任务)) {
            JSON返回(false, '已接受该任务');
        }
        if (in_array($任务名, $已完成任务)) {
            JSON返回(false, '已完成该任务');
        }
        
        $已接任务[] = $任务名;
        $用户数据['任务']['已接任务'] = implode(',', $已接任务);
        
        if (!isset($用户数据['任务进度'])) {
            $用户数据['任务进度'] = [];
        }
        $用户数据['任务进度'][$任务名] = 0;
        
        写入用户数据($用户名, $用户数据);
        
        JSON返回(true, "接受任务：{$任务名}");
        break;
        
    case '获取已接任务':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $已接任务 = isset($用户数据['任务']['已接任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已接任务']))) : [];
        $任务配置 = 读取配置('任务配置');
        
        $任务列表 = [];
        foreach ($已接任务 as $任务名) {
            if (isset($任务配置[$任务名])) {
                $任务 = $任务配置[$任务名];
                $进度 = isset($用户数据['任务进度'][$任务名]) ? intval($用户数据['任务进度'][$任务名]) : 0;
                $目标数量 = isset($任务['目标数量']) ? intval($任务['目标数量']) : 1;
                
                $任务列表[] = [
                    '名称' => $任务名,
                    '描述' => $任务['描述'],
                    '类型' => $任务['类型'],
                    '进度' => $进度,
                    '目标' => $目标数量,
                    '奖励经验' => $任务['奖励经验'],
                    '奖励货币' => $任务['奖励货币']
                ];
            }
        }
        
        JSON返回(true, '获取成功', $任务列表);
        break;
        
    case '提交任务':
        if (!isset($_SESSION['用户名'])) {
            JSON返回(false, '请先登录');
        }
        
        $任务名 = isset($_POST['任务名']) ? $_POST['任务名'] : '';
        $任务配置 = 读取配置('任务配置');
        
        if (!isset($任务配置[$任务名])) {
            JSON返回(false, '任务不存在');
        }
        
        $用户名 = $_SESSION['用户名'];
        $用户数据 = 读取用户数据($用户名);
        
        $已接任务 = isset($用户数据['任务']['已接任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已接任务']))) : [];
        $已完成任务 = isset($用户数据['任务']['已完成任务']) ? array_filter(array_map('trim', explode(',', $用户数据['任务']['已完成任务']))) : [];
        
        if (!in_array($任务名, $已接任务)) {
            JSON返回(false, '未接受该任务');
        }
        
        $任务 = $任务配置[$任务名];
        $进度 = isset($用户数据['任务进度'][$任务名]) ? intval($用户数据['任务进度'][$任务名]) : 0;
        $目标数量 = isset($任务['目标数量']) ? intval($任务['目标数量']) : 1;
        
        if ($任务['类型'] == '对话') {
            $完成 = true;
        } elseif ($任务['类型'] == '打怪') {
            $完成 = $进度 >= $目标数量;
        } elseif ($任务['类型'] == '收集') {
            $目标物品 = $任务['目标物品'];
            $物品数量 = isset($用户数据['背包'][$目标物品]) ? intval($用户数据['背包'][$目标物品]) : 0;
            $完成 = $物品数量 >= $目标数量;
            if ($完成) {
                从背包移除物品($用户数据, $目标物品, $目标数量);
            }
        } else {
            $完成 = $进度 >= $目标数量;
        }
        
        if (!$完成) {
            JSON返回(false, '任务尚未完成');
        }
        
        $已接任务 = array_diff($已接任务, [$任务名]);
        $已完成任务[] = $任务名;
        $用户数据['任务']['已接任务'] = implode(',', $已接任务);
        $用户数据['任务']['已完成任务'] = implode(',', $已完成任务);
        
        $用户数据['角色']['经验'] += intval($任务['奖励经验']);
        $用户数据['角色']['货币'] += intval($任务['奖励货币']);
        
        if (!empty($任务['奖励物品'])) {
            $奖励数量 = isset($任务['奖励物品数量']) ? intval($任务['奖励物品数量']) : 1;
            添加物品到背包($用户数据, $任务['奖励物品'], $奖励数量);
        }
        
        检查升级($用户数据);
        写入用户数据($用户名, $用户数据);
        
        $奖励消息 = "任务完成！获得经验：{$任务['奖励经验']}，获得货币：{$任务['奖励货币']}";
        if (!empty($任务['奖励物品'])) {
            $奖励消息 .= "，获得物品：{$任务['奖励物品']} x{$任务['奖励物品数量']}";
        }
        
        JSON返回(true, $奖励消息);
        break;
        
    default:
        JSON返回(false, '无效的操作');
        break;
}
?>
